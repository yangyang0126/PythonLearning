"""
两个3行3列的矩阵，通过代码实现：
输出一个新的3行3列矩阵，每个位置的数据，是前两个矩阵对应位置数据相加
x= [[12,7,3],
    [4,5,6],
    [7,8,9]]
y = [[5,8,1],
     [6,7,3],
     [4,5,9]]
"""

import numpy as np
x= [[12,7,3],
    [4,5,6],
    [7,8,9]]
y = [[5,8,1],
     [6,7,3],
     [4,5,9]]
xy = np.array(x)+np.array(y)
print(xy)

